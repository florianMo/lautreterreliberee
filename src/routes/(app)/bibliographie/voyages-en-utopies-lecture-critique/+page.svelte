<p class="text-base lg:text-xl my-10 italic">
    Les publications concernant Louis Rimbault et «Terre Libérée» ne sont pas
    legion et, pour la plupart, datent un peu. Les dernières mises au point issues des
    recherches dans les archives et les périodiques anarchistes et naturiens ont été
    entreprises par l’association « L’Autre Terre Libérée » en 2022-2024. Ces recherches
    participent de la connaissance historique et s’appuient sur les méthodes de
    l’historien. La sortie de «Voyages en utopies» relève d’une tout autre demarche puisqu’il
    s’agit d’un travail journalistique dont le but, la méthode et la rigueur ne sont pas ceux
    de l’historien : «Depuis longtemps les historiens savent que mener des recherches, ce
    n'est pas seulement savoir lire et écrire mais surtout savoir interpréter, comprendre,
    déduire, généraliser, relativiser, etc..., bref opérer un certain nombre de démarches
    intellectuelles face à un certain nombre d'informations et de situations.»<RefLink number={1}/>
</p>

<Title2>
    L'utopie
</Title2>

<Paragraph>
    La démarche utilisée dans ces Voyages en utopies<RefLink number={2}/> a suscité de notre part plusieurs réflexions et commentaires.
</Paragraph>
<Paragraph>
    L’ambition de réaliser un tour d’horizon mondial de plusieurs «utopies»
    historiques et actuelles a, d’emblée, attiré notre attention mais plusieurs chapitres<RefLink number={3}/>
    nous ont étonnés tant sur le plan du contexte historique que de l’écriture
    journalistique.
</Paragraph>
<Paragraph>
    Rappelons tout d’abord quelques éléments contextuels. Une « utopie » ne
    saurait définir tout projet sociétal imaginaire, reve, voire irréaliste surtout s’il est
    fonde sur des principes conservateurs et inégalitaires. L’historien Michel Winock l’a
    precisé : « L’utopie est d’abord une manière détournée de critique sociale. (...) Si le
    contenu des utopies peut varier, toutes se caractérisent d’abord par l’harmonie,
    l’équilibre, les mécanismes de régulation. Et pour presque toutes, la tendance dominante
    est l’égalité sociale enfin accomplie, grâce à la disparition de la propriété privée, tenue
    par maints auteurs pour la cause du malheur des hommes. (...) Le corollaire de l’utopie,
    il faut y venir, est la protestation morale, sociale contre le monde tel qu’il est, contre son
    principe régissant d’inégalité. »<RefLink number={4}/> Le lien utopie-socialisme est donc avéré (« la
    propriété mère de tous les crimes »<RefLink number={5}/> écrivait Morelly en 1753) et exclut, de fait, tout
    projet d’essence capitaliste (« la Terre comme une nourrice commune »<RefLink number={6}/>).
    Originellement, c’est-à-dire au moment de la Renaissance humaniste, l’utopisme
    nouveau-né est une critique du politique réel dominant. « Le genre utopique est un
    point cardinal de toute théorie politique : en tant qu’idéal, mais aussi en tant que moteur
    de l’action politique, puisqu’elle ouvre, à l’intérieur du réel, la possibilité d’une analyse
    critique radicale, voire de la révolution. À cet égard, l’utopie fait aussi paradoxalement
    figure de principe de réalité, en ramenant régulièrement le politique à ses véritables
    enjeux : le bien commun, la liberté, et à travers eux, le bonheur individuel. »<RefLink number={7}/> Associer,
    comme le font les auteurs de « Voyages en utopies », les anarchistes Louis Rimbault ou
    Giovanni Rossi au fasciste d’Annunzio, au capitaliste admirateur du nazisme Henry
    Ford et au nationaliste antisémite Bernhard Forster ne laisse pas de surprendre. Ces
    amalgames relèvent d’une méconnaissance historique des éléments fondateurs de
    l’utopisme comme socialisme rêvé et portent clairement préjudice à la
    compréhension d’un mouvement qui, à partir de la Révolution Française et de
    Gracchus Babeuf, a donné naissance à de nombreuses experiences empiriquement
    constitutives du plus grand mouvement politique de la fin du XIXe et du XXe siecle<RefLink number={8}/> :
    le socialisme. Ce n’est pas parce que la droite conservatrice etatsunienne – relayée par
    quelques intellectuels français, pour la plupart ex-staliniens purs et durs - a poursuivi
    sa guerre froide en faisant des Lumières du XVIIIe siècle et des utopies du XIXe siècle
    la matrice d’un « totalitarisme » associant « communisme » et « nazisme », qu’il faut
    la suivre sur le terrain boueux d’un mauvais révisionnisme aboutissant à la formule
    de François Furet : « Nous voici condamnés à vivre dans le monde où nous vivons. »<RefLink number={9}/>
</Paragraph>

<Title2>Un matériau linguistique très connoté</Title2>

<Paragraph>
    La rentabilité, la brièveté et l’attractivité auxquels sont soumis (ou se
    conforment) les journalistes se traduisent jusque dans la terminologie utilisée. Si cela
    obscurcit l’information dans le cadre d’un article d’actualité, cela devient hors de
    propos lorsqu’il s’agit d’endosser le rôle de « médiateur » de l’histoire et empêche
    toute intelligibilité des faits et des acteurs du passe.
</Paragraph>
<Paragraph>
    Le chapitre consacré à « Terre Libérée » porte ainsi le titre parfaitement subjectif
    de « la vaine Cité végétalienne de Louis Rimbault ». Mis à part le fait qu’aucune utopie
    n’a vecu dans la longue durée, le lecteur désireux de s’informer objectivement sur
    cette colonie libertaire en a déjà pour ses frais : elle fut dérisoire et inefficace. Oraison
    funèbre donc qui ne saurait remplacer l’étude historique et la démonstration de cette
    entrée en matière pour le moins expéditive.
</Paragraph>
<Paragraph>
    Les 3e et 4e cases de la 2e planche de la bande dessinée illustrant ce chapitre<RefLink number={10}/>
    représentent Louis et Clémence en grande discussion – dont on pourra, au demeurant,
    questionner l’intérêt - au sujet de la menthe devant entrer ou non dans la composition
    de la Basconnaise, « salade d’infinie variété végétale » élaborée par Rimbault dans la
    colonie de Bascon<RefLink number={11}/> :

    <ul>
        <li>Louis : « Mais c'est aromatique, c'est du poison capitaliste »</li>
        <li>Clémence : « Tu exagères : c’est juste de la menthe, c’est rafraîchissant. »</li>
        <li>Louis : « Avec toi, c’est toujours pareil, pense à l’anarchie ! »</li>
    </ul>

    Un dialogue imaginaire, même aussi caricatural, aurait pu faciliter la compréhension
    du végétalisme du couple. Ce n’est pas le cas pour une simple raison : il est facile de se
    procurer la composition de la Basconnaise en consultant les archives<RefLink number={12}/> ou <a href="/balade-sonore/6" target="_blank">le site de
    « L’Autre Terre Libérée »</a> et vérifier que les plantes aromatiques ne sont nullement
    proscrites par Rimbault : parmi les nombreux ingrédients de la Basconnaise on
    trouve, comme « poisons capitalistes », ces « condiments associés au choix : poireaux,
    ciboulette, oignons, ail - vert de préférence, pourpier, oseille, persil, cerfeuil, estragon,
    fenouil, sarriette, raiponce, pimprenelle, champignons crus et fleurs de trèfle, de sainfoin,
    luzerne ou capucines, roses, genêts, coquelicot, sureau. »<RefLink number={13}/> Cette période du « milieu
    libre » de Bascon, précédant la fondation de « Terre Libérée », est assez bien
    documentée par Celine Beaudet et Tony Legendre<RefLink number={14}/> et un minimum de rigueur aurait
    pu éviter d’induire le lecteur en erreur.
</Paragraph>

<Paragraph>
    Poursuivant la lecture du texte, nous apprenons par ailleurs que « ce moustachu
    au front large (sic) a acquis pour la somme de 160 000 francs un grand terrain... »<RefLink number={15}/>.
    Récit d’agrément journalistique mais information intéressante (je ne parle pas de la
    moustache) et somme considérable. Quelle est sa source ? Nous n’en saurons rien pas
    plus que l’origine des fonds nécessaires à l’achat du terrain des Pins a Luynes (fortune
    personnelle, dons, actionnariat coopératif... ?).
</Paragraph>

<Title2>
    Les mots et les choses
</Title2>

<Paragraph>
    Par contre, ce que nous découvrons c’est l’utilisation future de ce terrain par
    Rimbault et les siens : «... vivre en autonomie sur la base des principes extrémistes qu’ils
    ont épousés... »<RefLink number={16}/> Existe-t-il un terme plus dénué de pertinence politique que celui
    d’extrémiste<RefLink number={17}/> ? Anarchisme, naturisme, végétalisme sont l’extrême de quoi ? Car
    enfin quels sont ces principes ? Cultiver son jardin, vivre au plus près de la « nature »,
    exclure les aliments les plus toxiques de son alimentation, se soigner par les plantes,
    éduquer les habitants... On peut évidemment les discuter sans les affubler d’un
    qualificatif inutile et insidieux. Il resterait à démontrer qu’un autre mode de vie aurait
    plus de « nuance » : si les principes de « Terre Libérée » sont « extremistes », comment
    qualifier dans ce cas ceux fondés sur l’esclavage, le travail forcé, l’exploitation de
    l’homme par l’homme, la destruction du monde vivant... ?<RefLink number={18}/> Le lexique médiatique de
    la novlangue neolibérale obscurcit à dessein la libre analyse de « Terre Libérée » par
    les lecteurs.
</Paragraph>

<Paragraph>
    Une brève mise au point historique, quelques lignes plus tard, se solde elle aussi
    par une malencontreuse assertion. Au lendemain de la période des attentats
    anarchistes des années 1890 et de la féroce répression qui s’en suivit (les « lois
    scélérates » dénoncées en leur temps par Jaurès), une partie des militants se serait
    tournée vers « les sirènes des nouvelles factions marxistes »<RefLink number={19}/>. Deux noms et un adjectif,
    deux connotations et une erreur. Des anarchistes rejoignant les marxistes – ou ceux
    désignés comme tels – voilà une évolution que personne jusque-là n’avait observé...
    sinon en sens contraire puisque les premiers (dont Louise Michel) se sont séparés de
    la Fédération des travailleurs socialistes de France en 1881. Relevons plutôt le terme
    de « sirène », aux connotations démagogique et séductrice négatives, pour se poser la
    question : de quelles « factions » parlent les auteurs ? S’il s’agit des différentes
    organisations socialistes existantes au début des années 1890 (autour d’Edouard
    Vaillant, Jules Guesde, Jean Allemane et Jean Jaurès), « un trait commun est la faible
    connaissance du marxisme et sa faible diffusion, même chez ceux qui s’en
    revendiquent... » explique Thomas Rose<RefLink number={20}/>. Le lecteur intéressé cherchera donc en vain
    ces nouveaux groupes marxistes de la fin du XIXe siècle et la place occupée en leur
    sein par les anarchistes<RefLink number={21}/>. Faire le lien entre les utopies et le marxisme aurait pourtant
    été intéressant puisque Marx et Engels, tout en critiquant le socialisme utopique
    comme réactionnaire au regard de l’histoire, s’intéressaient de près aux expériences
    utopiques en tant qu’exemples ou modèles de sociétés débarrassées de la propriété
    privée et de l’Etat<RefLink number={22}/>.
</Paragraph>

<Title2>
    Radicaux et forcenés
</Title2>

<Paragraph>
    Revenons aux anarchistes tendance Rimbault. Non, ce ne sont pas des
    « activistes décriés comme individualistes »<RefLink number={23}/> ainsi que l’affirment les auteurs pour la
    bonne et simple raison qu’ils se réclament eux-mêmes de l’individualisme dans le
    sillage de Max Stirner<RefLink number={24}/>, John Henry Mackay<RefLink number={25}/> et E. Armand, auteur du Petit Manuel
    anarchiste individualiste (1911) et en font un étendard revendiqué : « L’anarchiste-
    individualiste ne considère l’association que comme un expédient, un pis-aller. Il ne veut
    donc s’associer qu’en cas d’urgence mais toujours volontairement. [...] Il ne pense pas
    que les maux dont souffrent les hommes proviennent exclusivement du capitalisme ou
    de la propriété privée. Il pense qu’ils sont dus surtout à la mentalité défectueuse des
    hommes, pris en bloc. »<RefLink number={26}/> Est-ce suffisant pour qualifier cette tendance du mouvement
    anarchiste, pas moins respectable qu’une autre, de « cinquième colonne »<RefLink number={27}/>, c’est-à-
    dire de bande d’infiltrés, d’ennemis de l’intérieur agissant dans l’ombre alors qu’ils se
    sont, a contrario, délibérément mis « en-dehors » de la société et du mouvement
    social sans aucun projet révolutionnaire (ce qui les opposait aux communistes
    libertaires et aux syndicalistes révolutionnaires).
</Paragraph>

<Paragraph>
    Au même titre que le terme « extrêmiste », celui de « radical » demeure souvent
    aussi confus, inopérant et surtout anachronique en cette fin de siècle ou, en France,
    un parti qualifié de « radical »<RefLink number={28}/> allait représenter – malgre ses références aux idéaux
    de la Gauche - les intérêts de la petite bourgeoisie républicaine. Les « lois naturelles »<RefLink number={29}/>
    chères aux anarchistes individualistes relèvent-elles donc, comme l’expliquent les
    auteurs, de la « conception radicale »<RefLink number={30}/> qu’en aurait eue Bakounine ? Qu’y a-t-il de
    « radical », au sens contemporain du terme, dans ces mots de l’anarchiste russe : « La
    liberté de l'homme consiste uniquement en ceci qu'il obéit aux lois naturelles parce qu'il
    les a reconnues lui-même comme telles, et non parce qu'elles lui ont été imposées par
    une volonté étrangère, divine ou humaine, collective ou individuelle, quelconque. »<RefLink number={31}/> Il
    faut aller aux textes pour se pénétrer de la pensée d’une personne ou d’un groupe
    avant de l’estampiller mécaniquement selon une idée reçue.
</Paragraph>

<Paragraph>
    Puisqu’après la Grande Guerre Rimbault « est devenu un des leaders de ce
    bataillon de forcenés du soulèvement social »<RefLink number={32}/> pourquoi ne pas expliquer en quelques
    mots qu’il fut un des dirigeants des grèves parisiennes de 1919 en faveur de la
    semaine anglaise (44 heures de travail sur 5,5 jours), trahies par la direction de la
    Fédération des métaux C.G.T. ? Que signifie le terme de « forcenés » appliqué à la classe
    ouvrière en lutte pour défendre ses droits et revendications au lendemain de
    bénéfices colossaux capturés par les marchands de canon et profiteurs de guerre ?
    Est-il justifié de l’appliquer aux 150 000 métallos grévistes le 2 juin 1919 ? Le
    vocabulaire des auteurs reste toujours à sens unique lorsqu’il est question du
    mouvement social et de ses organisations.
</Paragraph>

<Title2>
    L'anarchisme méconnu
</Title2>

<Paragraph>
    Abordée en quelques mots, la question de la guerre – il s’agit de la Grande
    Guerre que, manifestement, les auteurs connaissent peu - n’a pas tout-à-fait
    « traumatisé jusqu’au plus solide des anars » (sauf ceux qui étaient au front et la
    poignée de pacifistes) parce qu’une bonne partie d’entre eux l’a soutenue et non des
    moindres : Kropotkine, Jean Grave, Charles Malato, Paul Reclus<RefLink number={33}/>..., à l’image des
    socialistes et de la majorité des syndicalistes.
</Paragraph>

<Paragraph>
    Si la grande œuvre de Sébastien Faure fut effectivement son Encyclopédie
    anarchiste à laquelle collabora Rimbault (1925-1934)<RefLink number={34}/>, il n’est certainement pas, au
    cours de ses années d’après-guerre, « la star de la scène libertaire »<RefLink number={35}/> mais plutôt un
    « vieux sage de l’anarchisme » comme le rappelle le Maitron, une figure de référence
    et un journaliste toujours actif dans les organisations anarchistes<RefLink number={36}/>.
</Paragraph>

<Paragraph>
    Le décès de Clémence en 1927 peut-il, au-delà d’une tuberculose, être relié à la
    période des « bandits tragiques » auxquels Rimbault fut mêlé en 1911 et qui l’aurait
    durablement atteinte au point qu’elle en meure 16 ans plus tard ? Peut-on
    raisonnablement qualifier, dans la foulée, de « terrorisme d’extrême-gauche »<RefLink number={37}/> les
    actions de la bande à Bonnot qui, bien que se réclamant de l’anarchisme et ayant des
    liens avec plusieurs militants, s’était délibérément exclue du mouvement ouvrier ?
    Nos auteurs poursuivent-ils le travail de leurs confrères de l’époque à l’origine de
    cette « panique morale créée en grande partie par la surenchère médiatique, cette
    presse avide de sang et de mélodrame que dénonce Jaurès et qui fait de la bande à
    Bonnot (les fameux « bandits en auto », auteurs d’une série de vols en bande organisée
    et se revendiquant de l’anarchisme) l’incarnation des périls censés menacer la société
    française »<RefLink number={38}/> ?
</Paragraph>

<Paragraph>
    Sans procéder systématiquement à une analyse sémantique, relevons enfin ce
    qui se situe hors du champ de la rigueur historique et fabrique une représentation de
    Rimbault et de ses camarades : « Rimbault a écouté l’influent Butaud déblatérer »,
    « voilà le graal après lequel courent maintenant Rimbault et ses semblables »,
    « évangélisation des populations aux principes de l’anarcho-naturisme »<RefLink number={39}/>. Ces
    raccourcis méprisants et trompeurs associent les anarchistes des « milieux libres » à
    des comportements et des pratiques violentes et sectaires qu’aucun document ni
    aucune source ne permettent d’étayer ou d’en faire des caractéristiques permanentes.
    La partie consacrée a la naissance de « Terre Libérée » et à son installation s’avère plus
    objective, sans dérapage lexical et rend assez bien compte des activités de la petite
    communauté (p. 141-142). Il est donc dommage que, sur une question d’histoire déjà
    beaucoup étudiée dans sa diversité et comprenant des références incontournables, le
    chapitre sur « Terre Libérée », a priori prometteur, ne soit en definitive qu’un survol
    simpliste, reducteur, aux idées reçues, laissant un goût amer éloigné de la rigueur
    scientifique qu’il aurait méritée.
</Paragraph>

