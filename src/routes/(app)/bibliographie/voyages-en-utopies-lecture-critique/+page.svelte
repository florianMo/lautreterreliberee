<h1 class="text-center frankie text-4xl mb-4">Une lecture critique de «Voyages en utopies»<br/>de Benoist Simmat et Daniel Casanave<br/>(Fayard Graffik, 2025)</h1>

<div class="flex justify-center my-4">
