<h2 class="frankie text-2xl py-4">Chronologie de G. Davranche : «Un printemps d’occasions manquées»</h2>
<ul>
    <li><span class="font-bold">23 avril</span> Vote des huit heures.</li>
    <li><span class="font-bold">24 avril</span> Le Comité d’entente de la métallurgie parisienne maintient la revendication de la semaine anglaise avec hausse des salaires.
    </li>
    <li><span class="font-bold">1er mai</span> Manifestation énorme. Nombreux blessés, un mort. La CGT n’appelle pas à la grève générale.
    </li>
    <li><span class="font-bold">27 mai</span> La CGT décide de coorganiser avec les belges, les italiens et les britanniques une grève générale pour 1) la démobilisation rapide et totale ; 2)
        l’amnistie des pacifistes ; 3) la non-intervention militaire en Russie et en Hongrie. Les négociations pour trouver une date commune vont traîner des semaines. Ultimatum du Comité d’entente de
        la métallurgie parisienne au patronat.
    </li>
    <li><span class="font-bold">1er juin</span> Le Comité d’entente appelle à la grève. Raz-de-marée immédiat : 170 000 grévistes, bientôt suivis par 20 000 dans les transports publics et quelques
        milliers de peintres.
    </li>
    <li><span class="font-bold">4 juin</span> Les grévistes de Saint-Denis somment « le gouvernement de remettre le pouvoir entre les mains de la classe ouvrière ».
    </li>
    <li><span class="font-bold">8 juin</span> Le Comité d’entente rompt les négociations avec le patronat qui refuse l’augmentation des salaires et l’égalite hommes-femmes ; met en place des « soupes
        communistes » pour les grévistes ; demande à la fédération de lancer une grève nationale.
    </li>
    <li><span class="font-bold">11 juin</span> La fédération rejette la grève nationale par 6 voix contre 4.</li>
    <li><span class="font-bold">15 juin</span> Les transports publics reprennent le travail.</li>
    <li><span class="font-bold">17 juin</span> Premiers signes de reprise dans certaines entreprises. Au Comité d’entente, certains veulent maintenir la grève sur le terrain exclusivement corporatif ;
        d’autres veulent affirmer son caractère révolutionnaire. Finalement on décide de maintenir les revendications économiques, mais d’interpeler de nouveau la fédération pour une grève nationale.
    </li>
    <li><span class="font-bold">22 juin</span> Le Comite d’action envahit la réunion de la fédération des Métaux en exigeant l’appel a la grève nationale. La fédération refuse et renvoie la balle au
        Cartel interfédéral (dockers, mineurs, cheminots, métallos) pour une grève generale.
    </li>
    <li><span class="font-bold">25 juin</span> Le Cartel interfédéral refuse d’appeler a la grève générale. Découragement des grévistes.</li>
    <li><span class="font-bold">27 juin</span> Un meeting du Comité d’action décide la reprise du travail.</li>
    <li><span class="font-bold">28 juin</span> Le Comite d’entente appelle à la reprise. Capitulation sans conditions.</li>
    <li><span class="font-bold">20 juillet</span> La CGT annule la grève générale internationale du 21 juillet.</li>
</ul>